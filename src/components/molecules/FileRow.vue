<template>
  <tr :class="{ 'table-active': file.select }" @click="toggleSelect">
    <td class="text-center">
      <input type="checkbox" :checked="file.select" />
    </td>
    <td>
      <div class="d-flex">
        <nuxt-link :to="`/files/${file.id}`">
          {{ file.name }}
        </nuxt-link>
      </div>
    </td>
    <td>{{ date }}</td>
    <td>
      <span class="thumb ml-auto">
        <img v-if="file.image_info" :src="file.link" height="24" />
      </span>
    </td>
  </tr>
</template>
<script lang="ts">
import Vue, { PropOptions } from 'vue'
import { ModifiedFile } from '~/components/file-list.vue'
export default Vue.extend({
  props: {
    file: {
      type: Object,
      required: true,
    } as PropOptions<ModifiedFile>,
  },
  computed: {
    date(): string {
      return this.$dayjs(this.file.created_at).format('LLL')
    },
  },
  methods: {
    toggleSelect() {
      this.$emit('update:file', {
        ...this.file,
        select: !this.file.select,
      })
    },
  },
})
</script>
